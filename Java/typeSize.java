
public class typeSize {
    
    public static void main(String[] args){

        // There are built-in constants to print maximum, minimum and size of a type.
        // for int..

        System.out.println("int minimum: " + Integer.MIN_VALUE);
        System.out.println("int maximum: " + Integer.MAX_VALUE);
        System.out.println("int size: " + Integer.SIZE);

        System.out.println();
        // for float..
        System.out.println("flaot minimum: " + Float.MIN_VALUE);
        System.out.println("float maximum: " + Float.MAX_VALUE);
        System.out.println("float size: " + Float.SIZE);
    }
}
